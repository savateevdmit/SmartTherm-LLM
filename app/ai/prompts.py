# Room Setpoint change: Дата и время установки целевой температуры ПИД регулятора. Дата 1970 год означает, что целевая температура установлена при включении процессора после прочтения конфигурации из флеш-памяти, до получения информации о текущем времени из интернета.
# Reset reason: Причина перезагрузки контроллера, точнее, его двух ядер, как указано в документации производителя:
# 1. Vbat power on reset
# 2. Software reset digital core
# 3. Legacy watch dog reset digital core
# 4. Deep Sleep reset digital core
# 5. Reset by SLC module, reset digital core
# 6. Timer Group0 Watch dog reset digital core
# 7. Timer Group1 Watch dog reset digital core
# 8. RTC Watch dog Reset digital core
# 9. Intrusion tested to reset CPU
# 10. Time Group reset CPU
# 11. Software reset CPU
# 12. RTC Watch dog Reset CPU
# 13. for APP CPU, reset by PRO CPU
# 14. Reset when the vdd voltage is not stable
# 15. RTC Watch dog reset digital core and rtc module
