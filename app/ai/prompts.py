#     # apt-get update
#     # apt-get install esptool
# 3. Обновить прошивку из той директории, где находится скачанный файл:
#     $ esptool -cd nodemcu -cf Smart_Therm_0.1.2_20220318.bin
# 4. Подробнее см. документацию к esptool.
#
# Особенности прошивки ESP WROOM-32
# Для некоторых вариантов ESP WROOM 32 для прошивки контроллера необходимо нажать кнопку Boot в момент начала работы загрузчика. Если прошивка происходит автоматически, то кнопку нажимать не надо.
