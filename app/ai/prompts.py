# WL_NO_SSID_AVAIL = 1,
# WL_SCAN_COMPLETED = 2,
# WL_CONNECTED = 3,
# WL_CONNECT_FAILED = 4,
# WL_CONNECTION_LOST = 5,
# WL_WRONG_PASSWORD = 6,
# WL_DISCONNECTED = 7
