#
# 9.2 Коды ошибок OpenTherm
#
# Наличие ошибки котла в OpenTherm определяется битом LB:0 в статусе котла (ID0). Код ошибки выдается по ID5 (ASFlags), кроме этого, есть еще ID115 (OEMDiagnosticCode).
#
# 9.2.1 ID0 (Status)
#
# В младшем байте статуса младший бит является индикатором наличия ошибки. Более подробную информацию об ошибках дает ID5 и ID115 (при наличии).
#
# Бит: ID0:LB0 — fault indication [ no fault, fault ]
# Описание: 0 – нет ошибки, 1 — есть ошибка
#
# 9.2.2 ID5 (ASFlags)
#
# Старший байт ID5, HB: Application-specific fault flags. Младший байт ID5, LB: OEM fault/error code.
#
# Биты ID5:HB (Application-specific fault flags):
# • ID5:HB0 — Service request [servicenotreq'd, servicerequired] Запрос обслуживания
# • ID5:HB1 — Lockout-reset [remoteresetdisabled, enabled] Блокировка-сброс
# • ID5:HB2 — Low water press [noWPfault, waterpressurefault] Низкое давление воды
# • ID5:HB3 — Gas/flame fault [noG/Ffault, gas/flamefault] Ошибка наличия газа/пламени
# • ID5:HB4 — Air press fault [noAPfault, airpressurefault] Ошибка давления воздуха
# • ID5:HB5 — Water over-temp [noOVTfault, over-temperat.Fault] Ошибка перегрева воды
# • ID5:HB6 — reserved
# • ID5:HB7 — reserved
#
# 9.2.3 ID115 (OEMDiagnosticCode)
#
# ID115 поддерживается не всеми котлами. Согласно спецификации OpenTherm содержит двухбайтный OEM-specific diagnostic/service code. Может совпадать с ID5:LB. Может частично совпадать с индикацией ошибки на панели котла.
#
