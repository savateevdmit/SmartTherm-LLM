# При включении контроллера или после нажатия Reset индикатор (светодиод) LED включается. После подключения к сети WiFi или после включения точки доступа контроллер переходит в режим циклического опроса OpenTherm. При этом, если соединения по OpenTherm нет, индикатор мигает коротким импульсом два раза в секунду (10 мс вкл, 500 мс выкл), если соединение есть — один раз в две секунды (10 мс вкл, 2000 мс выкл). Если соединение появилось, но потом пропало по каким-то причинам — длинным импульсом раз в две секунды (1000 мс вкл, 1000 мс выкл).
#
# 5.4 Подключение интерфейса OpenTherm на котле
#
# 1. Список газовых и электрических котлов, поддерживающих протокол OpenTherm: https://docs.google.com/spreadsheets/d/1wyp0VITQYxK9X20dXWBdqLty_onRzYsPemu9IB54h44/edit?usp=drive_link
# 2. Совместимость котлов с адаптерами Zont: https://www.tvp-electro.ru/sovmestimost-kotlov (выбрать марку котла, выбрать модель, в столбце «Тип управления: по цифровой шине» может находиться схема подключения. Если в этом столбце стоит прочерк, то с большой долей вероятности, у вашего котла нет интерфейса OpenTherm).
# 3. Схемы подключения котлов Baxi: https://baxi.ru/upload/iblock/2d1/6by6l35affwd3hxwcb_BAXI-Connect_-s-kotlami-BAXI.pdf
#
# Перед подключением необходимо ознакомиться в инструкции с тем, как это делается на вашем конкретном котле. Обычно для этого нужно найти разъем для подключения внешнего термостата, удалить из него перемычку и подключить провода для OpenTherm. В некоторых случаях разъем для подключения OpenTherm находится отдельно от разъема для перемычки термостата, в частности на некоторых моделях Baxi. Кроме этого может потребоваться выставить соответствующий параметр в инженерном меню или dip-переключатели на плате котла. Подробнее см. ниже.
#
# 1. Отключите питание на котле.
# 2. Снимите крышку котла, подключите шлейф интерфейса OpenTherm к контроллеру котла (двухжильным проводом) согласно инструкции на ваш котёл.
# 3. Прикрепите контроллер к посадочному месту, где он будет находиться, присоедините другой конец шлейфа интерфейса OpenTherm к контроллеру SmartTherm.
# 4. При необходимости присоедините внешние датчики температуры.
# 5. Присоедините шлейф питания micro USB/type C к контроллеру SmartTherm.
# 6. Закройте крышку котла, включите питание на котле.
# 7. Включите адаптер контроллера SmartTherm в сеть.
#
# Примечание: в зависимости от типа электронного управления котла возможно различное поведение котла при подключении интерфейса OpenTherm. Например, при положении перемычки «не использовать OpenTherm», данные по интерфейсу будут передаваться в режиме «только чтение», а при переключении перемычки в положение «использовать OpenTherm» управление с пульта самого котла становится невозможным.
#
# В большинстве случаев на котле OpenTherm подключается к обычному винтовому зажиму на колодке. Полярность значения не имеет.
#
# Однако у котлов ВАХI 4-го поколения (Eco Four, Eco Home, Eco 4s, Eco 5 Compact, Fourtech, Main5, Main Four) для подключения OpenTherm используется разъем MHU-2 (DS1074-2 F), розетка кабельная с контактами 5.08мм 2pin.
#
# Лайфхак от монтажников котлов для подключения к хитрым разъемам при отсутствии нужных розеток под рукой и наличии достаточного пространства для отвёртки. «Плата не Бакси, но, думаю, идея понятна».
