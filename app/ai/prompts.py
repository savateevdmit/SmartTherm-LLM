# Полная прошивка ESP32 с загрузчиком и разметкой файловой системы на flash:
#     esptool --chip esp32 --port "COM28" --baud 460800 --before default_reset --after hard_reset write_flash -z --flash_mode dio --flash_freq 40m --flash_size 4MB 0x1000 bootloader.bin 0x8000 partitions.bin 0xe000 boot_app0.bin 0x10000 firmware.bin
# Полная прошивка может потребоваться после установки LiveControl.
# Прошивка ESP8266:
#     esptool --before default_reset --after hard_reset --chip esp8266 --port "COM20" --baud 115200 write_flash 0x0 firmware.bin
