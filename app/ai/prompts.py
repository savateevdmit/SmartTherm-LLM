# RSSI: При подключении к сети WiFi - текущее значение сигнала WiFi в децибелах и в условных процентах, а также среднее за 10 минут.
#
# OpenTherm statistics: одиннадцать чисел — диагностируют сколько раз за время работы произошли следующие события (вариант на 7.11.2023):
# 1. Получен годный ответ на запрос (SUCCESS)
# 2. Получен негодный ответ на запрос, ошибка чётности
# 3. Не получено ответа на запрос (NONE)
# 4. Ответ на запрос получен, статус ответа — INVALID
# 5. Ответ на запрос не получен, статус TIMEOUT
# 6. Ответов на запрос Application-specific fault flags с ненулевым значением флага
# 7. Ответов на запрос OEMDiagnosticCode с ненулевым значением кода
# 8. Ошибка данных (DATA_INVALID)
# 9. Котел не поддерживает такой запрос (UNKNOWN_DATA_ID)
# 10. Ошибка в типе сообщения
# 11. Получен ответ, ID ответа не совпадает с ID запроса
