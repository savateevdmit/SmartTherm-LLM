# Большие числа для п. 1, 3, 5, 8, 10 говорят о проблемах с передачей данных.
#
# Время: текущее локальное время устройства.
# Вкл горелки: число включений горелки: всего, за час, за предыдущий час, за сутки, за предыдущие сутки.
# Эффективная модуляция: Средняя модуляция за час, за предыдущий час, за сутки. Эффективная модуляция позволяет оценить среднюю мощность котла для случаев, когда котел отдает реальное значение модуляции в процентах от номинальной мощности, а не ноль при выключенной горелке и ноль при минимальной модуляции.
