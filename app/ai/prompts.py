# 9.6 Сброс ошибок котла
#
# Не всякую ошибку котла можно сбросить программным способом. Например, ошибку «отсутствие газа», или «низкое давление теплоносителя». Некоторые ошибки можно сбросить на сервисной панели или программным способом через интерфейс OpenTherm. Сервисные инженеры достаточно негативно относятся к удаленному сбросу ошибок и рекомендуют давать себе ясный отчет о возможных последствиях.
#
# 9.6.1 Сброс ошибок перезагрузкой контроллера
#
# Некоторые ошибки котла можно сбросить перезагрузкой контроллера с инициализацией интерфейса OpenTherm. Перезагрузка контроллера выполняется через пункт меню «Reset...» в веб-интерфейсе.
#
# 9.6.2 Сброс ошибок через BLOR
#
# В пункте меню «Дополнительно» (Setup -> Дополнительно) возможно выполнение удаленного сброса ошибки путем подачи команды BLOR (“BLOR”= Boiler Lock-out Reset command, ID5:HB1). До окончания тестирования котла при старте контроллера сброс недоступен. Если при тестировании в ответ на ID5:HB0 не был получен положительный ответ (Command completed) кнопка сброса не будет отображаться.
#
# Если котел поддерживает эту функцию, то это не означает, что можно сбросить любую ошибку. Например, ошибку по давлению теплоносителя сбросить можно только путем открытия клапана подпитки. Ошибка по превышению количества попыток розжига сбросить можно. Рекомендуется использовать удаленный сброс ошибки с осторожностью, четко понимая что вы делаете.
